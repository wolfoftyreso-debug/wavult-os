// ─── Main App ─────────────────────────────────────────────────────────────────
export default function App() {
  const [user, setUser] = useState<HypebitUser | null>(null);
  const [activeView, setActiveView] = useState('overview');
  const [sidebarCollapsed, setSidebarCollapsed] = useState(false);
  const [notifOpen, setNotifOpen] = useState(false);

  function handleLogin(u: HypebitUser) {
    setUser(u);
    setActiveView(getDefaultView(u.role));
  }

  if (!user) return <LoginScreen onLogin={handleLogin} />;

  const SIDEBAR_W = sidebarCollapsed ? 60 : 220;

  function renderView() {
    switch (activeView) {
      case 'overview':   return <OverviewView />;
      case 'growth':     return <GrowthView />;
      case 'revenue':    return <RevenueView />;
      case 'product':    return <ProductView />;
      case 'automation': return <AutomationView />;
      case 'customers':  return <CustomersView />;
      case 'system':     return <SystemView />;
      default:           return <OverviewView />;
    }
  }

  const currentNav = NAV_ITEMS.find(n => n.id === activeView);

  return (
    <div style={{ display: 'flex', height: '100vh', background: C.bg, fontFamily: C.ui, color: C.text, overflow: 'hidden' }}>

      {/* ── Sidebar ── */}
      <div style={{
        width: SIDEBAR_W, flexShrink: 0, background: C.surface,
        borderRight: `1px solid ${C.border}`,
        display: 'flex', flexDirection: 'column',
        transition: 'width 0.2s ease', overflow: 'hidden',
      }}>
        {/* Logo */}
        <div style={{
          height: 56, display: 'flex', alignItems: 'center',
          padding: sidebarCollapsed ? '0 14px' : '0 18px',
          borderBottom: `1px solid ${C.border}`, flexShrink: 0,
          gap: 10, overflow: 'hidden',
        }}>
          <svg width="24" height="24" viewBox="0 0 32 32" fill="none" style={{ flexShrink: 0 }}>
            <rect width="32" height="32" rx="7" fill="#111827"/>
            <rect x="4" y="16" width="6" height="12" rx="1" fill="#3B82F6"/>
            <rect x="13" y="10" width="6" height="18" rx="1" fill="#8B5CF6"/>
            <rect x="22" y="4" width="6" height="24" rx="1" fill="#10B981"/>
          </svg>
          {!sidebarCollapsed && (
            <span style={{ fontSize: 16, fontWeight: 700, color: C.text, letterSpacing: '-0.3px', whiteSpace: 'nowrap' }}>hypebit</span>
          )}
          <div style={{ flex: 1 }} />
          <button onClick={() => setSidebarCollapsed(p => !p)} style={{
            background: 'none', border: 'none', cursor: 'pointer', color: C.text3,
            fontSize: 14, padding: 2, flexShrink: 0,
          }}>
            {sidebarCollapsed ? '→' : '←'}
          </button>
        </div>

        {/* Nav */}
        <nav style={{ flex: 1, padding: '10px 8px', overflow: 'hidden' }}>
          {NAV_ITEMS.map(item => {
            const accessible = canAccess(user.role, item.id);
            const active = activeView === item.id;
            return (
              <button key={item.id}
                onClick={() => accessible && setActiveView(item.id)}
                title={sidebarCollapsed ? item.label : ''}
                style={{
                  width: '100%', display: 'flex', alignItems: 'center', gap: 10,
                  padding: sidebarCollapsed ? '10px 0' : '9px 10px',
                  justifyContent: sidebarCollapsed ? 'center' : 'flex-start',
                  marginBottom: 2, borderRadius: 8, border: 'none', cursor: accessible ? 'pointer' : 'not-allowed',
                  background: active ? C.blueAlpha : 'none',
                  color: active ? C.blue : accessible ? C.text2 : C.text3,
                  fontFamily: C.ui, fontSize: 13, fontWeight: active ? 600 : 400,
                  transition: 'all 0.12s', overflow: 'hidden',
                  opacity: accessible ? 1 : 0.4,
                  borderLeft: active ? `2px solid ${C.blue}` : '2px solid transparent',
                }}
                onMouseEnter={e => { if (accessible && !active) (e.currentTarget as HTMLButtonElement).style.background = C.surface2; }}
                onMouseLeave={e => { if (!active) (e.currentTarget as HTMLButtonElement).style.background = 'none'; }}
              >
                <span style={{ fontSize: 15, flexShrink: 0, fontFamily: 'monospace' }}>{item.icon}</span>
                {!sidebarCollapsed && (
                  <span style={{ whiteSpace: 'nowrap' }}>{item.label}</span>
                )}
              </button>
            );
          })}
        </nav>

        {/* User area */}
        <div style={{
          padding: sidebarCollapsed ? '12px 8px' : '12px 14px',
          borderTop: `1px solid ${C.border}`, flexShrink: 0,
        }}>
          <div style={{ display: 'flex', alignItems: 'center', gap: 10, overflow: 'hidden' }}>
            <div style={{
              width: 30, height: 30, borderRadius: 7, background: C.blueAlpha,
              display: 'flex', alignItems: 'center', justifyContent: 'center',
              fontSize: 11, fontWeight: 700, color: C.blue, flexShrink: 0,
            }}>{user.avatar}</div>
            {!sidebarCollapsed && (
              <div style={{ flex: 1, minWidth: 0 }}>
                <div style={{ fontSize: 12, fontWeight: 600, color: C.text, whiteSpace: 'nowrap', overflow: 'hidden', textOverflow: 'ellipsis' }}>{user.name}</div>
                <div style={{ fontSize: 10, color: C.text3, whiteSpace: 'nowrap' }}>{user.role.toUpperCase()}</div>
              </div>
            )}
            {!sidebarCollapsed && (
              <button onClick={() => setUser(null)} style={{ background: 'none', border: 'none', cursor: 'pointer', color: C.text3, fontSize: 12, padding: 4, flexShrink: 0 }} title="Sign out">⎋</button>
            )}
          </div>
        </div>
      </div>

      {/* ── Main area ── */}
      <div style={{ flex: 1, display: 'flex', flexDirection: 'column', overflow: 'hidden' }}>

        {/* Topbar */}
        <div style={{
          height: 56, display: 'flex', alignItems: 'center',
          padding: '0 24px', borderBottom: `1px solid ${C.border}`,
          background: C.surface, flexShrink: 0, gap: 12,
        }}>
          <div style={{ flex: 1 }}>
            <div style={{ fontSize: 15, fontWeight: 600, color: C.text }}>{currentNav?.label || 'Dashboard'}</div>
            <div style={{ fontSize: 11, color: C.text3 }}>{currentNav?.desc}</div>
          </div>

          {/* Search */}
          <div style={{ position: 'relative' }}>
            <input placeholder="Search…" style={{
              height: 32, width: 200, padding: '0 12px 0 32px',
              background: C.surface2, border: `1px solid ${C.border}`,
              borderRadius: 8, color: C.text, fontSize: 13, outline: 'none', fontFamily: C.ui,
            }} />
            <span style={{ position: 'absolute', left: 10, top: '50%', transform: 'translateY(-50%)', color: C.text3, fontSize: 13 }}>⌕</span>
          </div>

          {/* Notifications */}
          <div style={{ position: 'relative' }}>
            <button onClick={() => setNotifOpen(p => !p)} style={{
              width: 34, height: 34, borderRadius: 8, background: C.surface2,
              border: `1px solid ${C.border}`, color: C.text2, fontSize: 15,
              cursor: 'pointer', display: 'flex', alignItems: 'center', justifyContent: 'center',
              position: 'relative',
            }}>
              🔔
              <span style={{
                position: 'absolute', top: 4, right: 4, width: 8, height: 8,
                background: C.blue, borderRadius: '50%', border: `2px solid ${C.surface}`,
              }} />
            </button>
            {notifOpen && (
              <div style={{
                position: 'absolute', top: 42, right: 0, width: 280,
                background: C.surface, border: `1px solid ${C.border2}`,
                borderRadius: 10, overflow: 'hidden', zIndex: 100,
                boxShadow: '0 8px 24px rgba(0,0,0,0.4)',
              }}>
                <div style={{ padding: '12px 16px', borderBottom: `1px solid ${C.border}`, fontSize: 12, fontWeight: 600, color: C.text2 }}>
                  Notifications
                </div>
                {DEMO_DATA.activity.slice(0, 3).map((a, i) => (
                  <div key={i} style={{ padding: '10px 16px', borderBottom: `1px solid ${C.border}`, display: 'flex', gap: 10 }}>
                    <span style={{ fontSize: 14 }}>{a.icon}</span>
                    <div>
                      <div style={{ fontSize: 12, color: C.text, lineHeight: 1.4 }}>{a.text}</div>
                      <div style={{ fontSize: 10, color: C.text3, marginTop: 2, fontFamily: C.mono }}>{a.time}</div>
                    </div>
                  </div>
                ))}
              </div>
            )}
          </div>

          {/* Avatar */}
          <div style={{
            width: 34, height: 34, borderRadius: 8, background: `linear-gradient(135deg, ${C.blue}, ${C.purple})`,
            display: 'flex', alignItems: 'center', justifyContent: 'center',
            fontSize: 12, fontWeight: 700, color: '#fff', cursor: 'pointer', flexShrink: 0,
          }}>
            {user.avatar}
          </div>
        </div>

        {/* Content */}
        <div style={{ flex: 1, overflowY: 'auto', padding: 24 }} onClick={() => notifOpen && setNotifOpen(false)}>
          <div style={{ maxWidth: 1200 }}>
            {renderView()}
          </div>
        </div>
      </div>
    </div>
  );
}
