// ─── Bar chart (CSS only) ─────────────────────────────────────────────────────
function BarChart({ data, color = C.blue, height = 120 }: {
  data: { month: string; mrr: number }[]; color?: string; height?: number;
}) {
  const max = Math.max(...data.map(d => d.mrr));
  return (
    <div style={{ display: 'flex', alignItems: 'flex-end', gap: 4, height, paddingBottom: 20, position: 'relative' }}>
      {data.map((d, i) => {
        const h = Math.round((d.mrr / max) * (height - 24));
        const isLast = i === data.length - 1;
        return (
          <div key={d.month} style={{ flex: 1, display: 'flex', flexDirection: 'column', alignItems: 'center', gap: 4 }}>
            <div
              title={`$${d.mrr.toLocaleString()}`}
              style={{
                width: '100%', height: h,
                background: isLast ? color : `${color}66`,
                borderRadius: '3px 3px 0 0',
                transition: 'background 0.15s',
                cursor: 'default',
                borderTop: isLast ? `2px solid ${color}` : 'none',
              }}
              onMouseEnter={e => { (e.currentTarget as HTMLDivElement).style.background = color; }}
              onMouseLeave={e => { (e.currentTarget as HTMLDivElement).style.background = isLast ? color : `${color}66`; }}
            />
            <span style={{ fontSize: 9, color: C.text3, fontFamily: C.mono }}>{d.month}</span>
          </div>
        );
      })}
    </div>
  );
}

