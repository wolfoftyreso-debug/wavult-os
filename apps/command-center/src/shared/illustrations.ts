// Get illustration URL for a module
export function getModuleIllustration(module: string, fallbackScenario = 117): string {
  const mapped = MODULE_ILLUSTRATIONS[module]
  if (mapped) return mapped.url
  const fallback = SCENARIOS.find(s => s.id === fallbackScenario)
  if (fallback) return fallback.url
  return `${CDN_BASE}/scenario_117.png`
}

// Get illustration by scenario number
export function getScenario(num: number): string {
  const found = SCENARIOS.find(s => s.id === num)
  if (found) return found.url
  const padded = String(num).padStart(3, '0')
  return `${CDN_BASE}/scenario_${padded}.png`
}

// Get all scenarios in a series (e.g., series 400 = scenarios 400-499)
export function getSeriesScenarios(series: number): ScenarioMeta[] {
  return SCENARIOS.filter(s => s.id >= series && s.id < series + 100)
}
