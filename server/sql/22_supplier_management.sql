-- ----------------------------------------------------------------------------
-- 3.2 supplier_evaluations — Periodic and event-driven supplier evaluations
-- Per ISO 8.4.1: organisations must evaluate and re-evaluate external providers.
-- Weighted scoring across six criteria produces a computed overall_rating.
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS supplier_evaluations (
  id                        UUID            PRIMARY KEY DEFAULT gen_random_uuid(),
  org_id                    UUID            REFERENCES organizations(id),
  supplier_id               UUID            NOT NULL REFERENCES suppliers(id),

  -- Evaluation header
  evaluation_type           evaluation_type NOT NULL,
  evaluation_date           DATE            NOT NULL,
  evaluator_id              UUID            NOT NULL REFERENCES profiles(id),
  period_from               DATE,
  period_to                 DATE,

  -- Scored criteria (1–5 scale)
  quality_score             INTEGER         CHECK (quality_score BETWEEN 1 AND 5),
  quality_weight            NUMERIC(3,2)    DEFAULT 0.30,

  delivery_score            INTEGER         CHECK (delivery_score BETWEEN 1 AND 5),
  delivery_weight           NUMERIC(3,2)    DEFAULT 0.25,

  responsiveness_score      INTEGER         CHECK (responsiveness_score BETWEEN 1 AND 5),
  responsiveness_weight     NUMERIC(3,2)    DEFAULT 0.15,

  compliance_score          INTEGER         CHECK (compliance_score BETWEEN 1 AND 5),
  compliance_weight         NUMERIC(3,2)    DEFAULT 0.15,

  price_score               INTEGER         CHECK (price_score BETWEEN 1 AND 5),
  price_weight              NUMERIC(3,2)    DEFAULT 0.10,

  innovation_score          INTEGER         CHECK (innovation_score BETWEEN 1 AND 5),
  innovation_weight         NUMERIC(3,2)    DEFAULT 0.05,

  -- Extensible custom criteria (array of {name, score, weight})
  custom_criteria           JSONB           DEFAULT '[]',

  -- Computed columns
  weighted_score            NUMERIC(5,2)    GENERATED ALWAYS AS (
    COALESCE(quality_score       * quality_weight,       0) +
    COALESCE(delivery_score      * delivery_weight,      0) +
    COALESCE(responsiveness_score * responsiveness_weight, 0) +
    COALESCE(compliance_score    * compliance_weight,    0) +
    COALESCE(price_score         * price_weight,         0) +
    COALESCE(innovation_score    * innovation_weight,    0)
  ) STORED,

  overall_rating            overall_rating  GENERATED ALWAYS AS (
    CASE
      WHEN (
        COALESCE(quality_score       * quality_weight,       0) +
        COALESCE(delivery_score      * delivery_weight,      0) +
        COALESCE(responsiveness_score * responsiveness_weight, 0) +
        COALESCE(compliance_score    * compliance_weight,    0) +
        COALESCE(price_score         * price_weight,         0) +
        COALESCE(innovation_score    * innovation_weight,    0)
      ) >= 4.5 THEN 'A'::overall_rating
      WHEN (
        COALESCE(quality_score       * quality_weight,       0) +
        COALESCE(delivery_score      * delivery_weight,      0) +
        COALESCE(responsiveness_score * responsiveness_weight, 0) +
        COALESCE(compliance_score    * compliance_weight,    0) +
        COALESCE(price_score         * price_weight,         0) +
        COALESCE(innovation_score    * innovation_weight,    0)
      ) >= 3.5 THEN 'B'::overall_rating
      WHEN (
        COALESCE(quality_score       * quality_weight,       0) +
        COALESCE(delivery_score      * delivery_weight,      0) +
        COALESCE(responsiveness_score * responsiveness_weight, 0) +
        COALESCE(compliance_score    * compliance_weight,    0) +
        COALESCE(price_score         * price_weight,         0) +
        COALESCE(innovation_score    * innovation_weight,    0)
      ) >= 2.5 THEN 'C'::overall_rating
      WHEN (
        COALESCE(quality_score       * quality_weight,       0) +
        COALESCE(delivery_score      * delivery_weight,      0) +
        COALESCE(responsiveness_score * responsiveness_weight, 0) +
        COALESCE(compliance_score    * compliance_weight,    0) +
        COALESCE(price_score         * price_weight,         0) +
        COALESCE(innovation_score    * innovation_weight,    0)
      ) >= 1.5 THEN 'D'::overall_rating
      ELSE 'F'::overall_rating
    END
  ) STORED,

  -- Objective performance data for the evaluation period
  nc_count_period           INTEGER         DEFAULT 0,
  complaint_count_period    INTEGER         DEFAULT 0,
  deliveries_total          INTEGER         DEFAULT 0,
  deliveries_on_time        INTEGER         DEFAULT 0,
  returns_count             INTEGER         DEFAULT 0,

  -- Decision & follow-up
  decision                  evaluation_decision,
  decision_rationale        TEXT,
  action_required           TEXT,
  follow_up_date            DATE,

  -- Linkages
  linked_nc_ids             UUID[]          DEFAULT '{}',
  linked_audit_id           UUID            REFERENCES audits(id),
  linked_improvement_id     UUID            REFERENCES improvements(id),
  notes                     TEXT,

  created_at                TIMESTAMPTZ     DEFAULT now()
);

