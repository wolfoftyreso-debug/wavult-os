-- ----------------------------------------------------------------------------
-- 4.3 Supplier NC counter + auto-watch escalation
-- When a new supplier_nc_register row is inserted:
--   a) increment suppliers.total_nc_count
--   b) if the supplier has >= 3 NCs in the last 90 days AND status = 'APPROVED'
--      automatically escalate status to 'ON_WATCH'
-- ----------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION trg_supplier_nc_inserted()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
DECLARE
  v_recent_nc_count INTEGER;
  v_current_status  supplier_status;
BEGIN
  -- a) Increment total NC counter
  UPDATE suppliers
  SET total_nc_count = total_nc_count + 1,
      updated_at     = now()
  WHERE id = NEW.supplier_id;

  -- b) Check recent NC frequency and escalate if threshold exceeded
  SELECT COUNT(*)
  INTO   v_recent_nc_count
  FROM   supplier_nc_register
  WHERE  supplier_id = NEW.supplier_id
    AND  created_at  >= now() - INTERVAL '90 days';

  SELECT status
  INTO   v_current_status
  FROM   suppliers
  WHERE  id = NEW.supplier_id;

  IF v_recent_nc_count >= 3 AND v_current_status = 'APPROVED' THEN
    UPDATE suppliers
    SET    status     = 'ON_WATCH',
           updated_at = now()
    WHERE  id = NEW.supplier_id;
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS supplier_nc_inserted ON supplier_nc_register;
CREATE TRIGGER supplier_nc_inserted
  AFTER INSERT ON supplier_nc_register
  FOR EACH ROW EXECUTE FUNCTION trg_supplier_nc_inserted();

