DO $$
DECLARE
  v_org_id          UUID;
  v_system_user_id  UUID;

  -- company IDs
  v_company_supabase   UUID;
  v_company_aws        UUID;
  v_company_stripe     UUID;
  v_company_vercel     UUID;
  v_company_github     UUID;
  v_company_anthropic  UUID;
  v_company_revolut    UUID;
  v_company_triggerdev UUID;
  v_company_expo       UUID;

BEGIN
  -- Resolve Wavult Groups org and a system user (first admin found, or NULL)
  SELECT id INTO v_org_id
  FROM organizations
  WHERE name ILIKE '%wavult%'
  LIMIT 1;

  SELECT id INTO v_system_user_id
  FROM profiles
  WHERE org_id = v_org_id
  ORDER BY created_at
  LIMIT 1;

  -- -------------------------------------------------------------------------
  -- 8.1 Companies — insert once, ignore conflicts on name
  -- -------------------------------------------------------------------------

  INSERT INTO companies (id, name, org_id, created_at)
  VALUES (gen_random_uuid(), 'Supabase, Inc.', v_org_id, now())
