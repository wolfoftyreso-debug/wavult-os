-- ----------------------------------------------------------------------------
-- 5.1 v_supplier_performance — per-supplier performance dashboard
-- Joins suppliers + companies and lateral-fetches the most recent evaluation
-- to expose live KPIs alongside reapproval status.
-- ----------------------------------------------------------------------------
CREATE OR REPLACE VIEW v_supplier_performance AS
SELECT
  s.id                        AS supplier_id,
  s.org_id,
  s.supplier_code,
  c.name                      AS supplier_name,
  s.status,
  s.category,
  s.criticality,
  s.current_rating,
  s.delivery_performance_pct,
  s.quality_performance_pct,
  s.total_nc_count,
  s.single_source,
  s.next_reapproval_date,
  CASE
    WHEN s.next_reapproval_date IS NULL            THEN 'OK'
    WHEN s.next_reapproval_date < CURRENT_DATE     THEN 'OVERDUE'
    WHEN s.next_reapproval_date <= CURRENT_DATE + 30 THEN 'DUE_SOON'
    ELSE 'OK'
  END                         AS reapproval_status,
  latest_eval.weighted_score  AS latest_score,
  latest_eval.overall_rating  AS latest_rating,
  latest_eval.evaluation_date AS latest_evaluation_date,
  latest_eval.decision        AS latest_decision
FROM suppliers s
JOIN companies c ON c.id = s.company_id
LEFT JOIN LATERAL (
  SELECT
    se.weighted_score,
    se.overall_rating,
    se.evaluation_date,
    se.decision
  FROM supplier_evaluations se
  WHERE se.supplier_id = s.id
  ORDER BY se.evaluation_date DESC, se.created_at DESC
  LIMIT 1
) latest_eval ON true;

-- ----------------------------------------------------------------------------
-- 5.2 v_supplier_risk — org-level risk aggregation
-- Provides a single-row summary per org_id for management dashboards and
-- ISO 8.4 risk monitoring reports.
-- ----------------------------------------------------------------------------
CREATE OR REPLACE VIEW v_supplier_risk AS
SELECT
  s.org_id,
  COUNT(*)                                                          AS total_suppliers,
  COUNT(*) FILTER (WHERE s.status = 'APPROVED')                    AS approved,
  COUNT(*) FILTER (WHERE s.status = 'ON_WATCH')                    AS on_watch,
  COUNT(*) FILTER (WHERE s.status = 'SUSPENDED')                   AS suspended,
  COUNT(*) FILTER (
    WHERE s.single_source = true
      AND s.criticality IN ('CRITICAL', 'HIGH')
  )                                                                 AS single_source_critical,
  COUNT(*) FILTER (
    WHERE s.next_reapproval_date IS NOT NULL
      AND s.next_reapproval_date < CURRENT_DATE
      AND s.status NOT IN ('DEACTIVATED', 'BLOCKED')
  )                                                                 AS reapproval_overdue,
  COUNT(*) FILTER (WHERE s.current_rating < 2.5)                   AS underperforming,
  ROUND(AVG(s.current_rating)::NUMERIC, 2)                         AS avg_rating
FROM suppliers s
GROUP BY s.org_id;

